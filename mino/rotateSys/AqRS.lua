function AqRS.getKickTable(player,name,ori,mode,fLib)
    if name=='O' then
        local t=fLib.coincide(player,0,-1)
        if mode=='R' and t then return AqRS.kickTable[name][mode] end
        if mode=='L' and t then return AqRS.kickTable[name][mode] end
        if mode=='F' and t then return AqRS.kickTable[name][mode] end
        return AqRS.kickTable[name]['default']
    else return AqRS.kickTable[name][mode][ori]
    end
end
